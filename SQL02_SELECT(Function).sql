-- 학번이 A112113인 김고운 학생의 년도 별 평점을 구하는 SQL문을 작성하시오.
-- 단, 이때 출력화면의 헤더는 "년도", "년도 별 평점"이라고 찍히게 하고, 점수는 반올림하여 소수점 이하 한자리까지만 표시한다.
SELECT EXTRACT(YEAR FROM TO_DATE(TERM_NO,'YYMM')) 년도, ROUND(AVG(POINT),1) "년도 별 평점"
FROM TB_GRADE
WHERE STUDENT_NO = 'A112113'
GROUP BY EXTRACT(YEAR FROM TO_DATE(TERM_NO,'YYMM'))
ORDER BY 년도;

SELECT SUBSTR(TERM_NO,1,4) AS 연도, ROUND(AVG(POINT),1) AS "연도별 평점"  
FROM TB_GRADE
WHERE STUDENT_NO = 'A112113'
GROUP BY SUBSTR(TERM_NO,1,4)
ORDER BY 연도;



