SELECT EMP_ID , EMP_NAME , PHONE , SALARY , JOB_NAME
FROM EMPLOYEE
JOIN JOB USING(JOB_CODE)
WHERE SALARY = (SELECT MAX(SALARY) 
				FROM EMPLOYEE
				WHERE EXTRACT (YEAR FROM HIRE_DATE) >= 2000);

